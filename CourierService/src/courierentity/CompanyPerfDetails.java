package courierentity;

public class CompanyPerfDetails {
	
	private String customerCount;
	private String deliveriesCount;
	private String sales;
	private String monthPerformance;
	public String getCustomerCount() {
		return customerCount;
	}
	public void setCustomerCount(String customerCount) {
		this.customerCount = customerCount;
	}
	public String getDeliveriesCount() {
		return deliveriesCount;
	}
	public void setDeliveriesCount(String deliveriesCount) {
		this.deliveriesCount = deliveriesCount;
	}
	public String getSales() {
		return sales;
	}
	public void setSales(String sales) {
		this.sales = sales;
	}
	public String getMonthPerformance() {
		return monthPerformance;
	}
	public void setMonthPerformance(String monthPerformance) {
		this.monthPerformance = monthPerformance;
	}
	
	

}
